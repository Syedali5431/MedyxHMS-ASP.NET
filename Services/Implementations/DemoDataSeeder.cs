        // ── Schema patches ───────────────────────────────────────────────────────

        private async Task PatchSchemaAsync()
        {
            // Drop the spurious FK from Appointments.StaffId -> Staff.Id.
            // This FK was auto-generated by EF Core convention but is incorrect;
            // StaffId is a computed shim that mirrors DoctorId.
            await _context.Database.ExecuteSqlRawAsync(@"
IF EXISTS (
    SELECT 1 FROM sys.foreign_keys
    WHERE name = N'FK_Appointments_Staff_StaffId'
      AND parent_object_id = OBJECT_ID(N'[dbo].[Appointments]')
)
BEGIN
    ALTER TABLE [dbo].[Appointments] DROP CONSTRAINT [FK_Appointments_Staff_StaffId];
END");
        }

