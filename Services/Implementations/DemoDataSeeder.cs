        // ── IPD Admissions ───────────────────────────────────────────────────────

        private async Task SeedIpdAdmissionsAsync()
        {
            if (await _context.IPDAdmissions.AnyAsync()) return;

            var patients = await _context.Patients.OrderBy(p => p.Id).Skip(5).Take(5).ToListAsync();
            var doctors  = await _context.Doctors.OrderBy(d => d.Id).ToListAsync();
            var beds     = await _context.Beds.Where(b => b.Status == "Available" && b.BedType == "General").Take(5).ToListAsync();

            if (!patients.Any() || !doctors.Any()) return;

            var admissionTypes = new[] { "Emergency", "Elective", "Referral" };
            var now = DateTime.UtcNow;

            var admissions = new List<IPDAdmission>();

            for (var i = 0; i < Math.Min(patients.Count, 5); i++)
            {
                var discharged   = i < 3;
                var admDate      = now.AddDays(-(10 - i * 2));
                var dischargeDate= discharged ? (DateTime?)admDate.AddDays(3 + i) : null;
                var bed          = beds.Count > i ? beds[i] : null;

                admissions.Add(new IPDAdmission
                {
                    PatientId      = patients[i].Id,
                    DoctorId       = doctors[i % doctors.Count].Id,
                    BedId          = bed?.Id,
                    AdmissionDate  = admDate,
                    DischargeDate  = dischargeDate,
                    AdmissionType  = admissionTypes[i % admissionTypes.Length],
                    Diagnosis      = "Acute condition requiring inpatient monitoring",
                    Treatment      = "IV fluids, monitoring, specialist review",
                    Notes          = string.Empty,
                    Status         = discharged ? "Discharged" : "Admitted",
                    DailyCharges   = bed?.DailyCharges ?? 500,
                    CreatedDate    = admDate,
                    CreatedBy      = "System",
                });

                // Mark bed as occupied for active admissions
                if (!discharged && bed != null)
                {
                    bed.Status    = "Occupied";
                    bed.PatientId = patients[i].Id;
                    bed.LastUpdated = now;
                }
            }

            await _context.IPDAdmissions.AddRangeAsync(admissions);
            await _context.SaveChangesAsync();
            _logger.LogInformation("Seeded {Count} IPD admissions", admissions.Count);
        }

        // ── Bills ────────────────────────────────────────────────────────────────

        private async Task SeedBillsAsync()
        {
            if (await _context.Bills.AnyAsync()) return;

            var patients = await _context.Patients.OrderBy(p => p.Id).Take(12).ToListAsync();
            if (!patients.Any()) return;

            var now  = DateTime.UtcNow;
            var bills = new List<Bill>();

            var serviceItems = new[]
            {
                ("Consultation Fee",     "Service",   200m),
                ("Blood Test (CBC)",     "Lab",       350m),
                ("X-Ray Chest",          "Radiology", 500m),
                ("ECG",                  "Radiology", 300m),
                ("Urine Analysis",       "Lab",       150m),
                ("Physiotherapy Session","Service",   400m),
                ("Dressing Change",      "Service",   120m),
                ("IV Cannulation",       "Service",   80m),
            };

            for (var i = 0; i < patients.Count; i++)
            {
                var billDate = now.AddDays(-(i + 1));
                var isPaid   = i < 8;
                var items    = new List<BillItem>();

                // 1-3 items per bill
                for (var j = 0; j <= i % 3; j++)
                {
                    var (name, type, price) = serviceItems[(i + j) % serviceItems.Length];
                    items.Add(new BillItem
                    {
                        ItemName    = name,
                        ItemType    = type,
                        Quantity    = 1,
                        UnitPrice   = price,
                        TotalPrice  = price,
                        Description = string.Empty,
                        CreatedDate = billDate,
                    });
                }

                var total   = items.Sum(x => x.TotalPrice);
                var paid    = isPaid ? total : (i == 8 ? Math.Round(total / 2, 2) : 0m);
                var pending = total - paid;
                var status  = isPaid ? "Paid" : (paid > 0 ? "Partially Paid" : "Unpaid");

                var bill = new Bill
                {
                    BillNumber  = $"BILL-{now.Year}-{(i + 1):D4}",
                    PatientId   = patients[i].Id,
                    BillDate    = billDate,
                    DueDate     = billDate.AddDays(30),
                    TotalAmount = total,
                    PaidAmount  = paid,
                    PendingAmount = pending,
                    Status      = status,
                    BillType    = "OPD",
                    Notes       = string.Empty,
                    CreatedDate = billDate,
                    CreatedBy   = "System",
                    BillItems   = items,
                };

                if (isPaid)
                {
                    bill.Payments.Add(new Payment
                    {
                        PaymentMethod = i % 3 == 0 ? "Cash" : (i % 3 == 1 ? "Card" : "Online"),
                        Amount        = total,
                        TransactionId = $"TXN{now.Year}{i:D6}",
                        PaymentGateway= string.Empty,
                        Status        = "Completed",
                        Notes         = string.Empty,
                        PaymentDate   = billDate.AddHours(1),
                        ProcessedBy   = "System",
                    });
                }

                bills.Add(bill);
            }

            await _context.Bills.AddRangeAsync(bills);
            await _context.SaveChangesAsync();
            _logger.LogInformation("Seeded {Count} bills", bills.Count);
        }
